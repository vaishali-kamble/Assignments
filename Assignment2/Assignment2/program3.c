#include<stdio.h>

void Display(int iNo)
{

    if(iNo<10)
    {
        printf("Hello \n");

    }
    else
    {
        printf("Demo \n");

    }
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Accept one number from user if number is less than 10 then print "Hello "on screen otherwise print "Demo"
/////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;

    printf("enter the number \n");
    scanf("%d",&iValue);

    Display(iValue);
    return 0;
}


