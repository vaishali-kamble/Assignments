#include<stdio.h>

void Accept(int iNo)
{
    int iCnt=0;

    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        printf("* \n");

    }
}
/////////////////////////////////////////////////////////////////
/// Accept one number from user and print that number * on screen
//////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    iValue=5;

    Accept(iValue);
    return 0;
}


