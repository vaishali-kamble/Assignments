#include<stdio.h>

void FactRev(int iNo)
{
    int iCnt=0;

    for(iCnt=iNo/2;iCnt>=1;iCnt--)
    {
        if(iNo%iCnt==0)
        {
            printf("%d \n",iCnt);
        }
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// write program to accept one number from user and display its factors in decreasing order.
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0;

    printf("Enter the number \n");
    scanf("%d",&iValue);

    FactRev(iValue);

    return 0;

}


