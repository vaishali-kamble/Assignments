#include<stdio.h>

int SumNonFact(int iNo)
{
    int iCnt=0;
    int iSum=0;

    for(iCnt=1;iCnt<=iNo;iCnt++)
    {
        if(iNo%iCnt!=0)
        {
           iSum=iSum + iCnt;
        }
    }

return iSum;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// write program to accept one number from user and return summation of its non factors .
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0;
    int iRet=0;

    printf("Enter the number \n");
    scanf("%d",&iValue);

    iRet=SumNonFact(iValue);
    printf("Summation of non factors :%d",iRet);

return 0;

}


